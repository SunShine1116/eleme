       <li> <h3>其他问题</h3> 
        <ol class="ouranswer-sub-list"> 
         <li id="Q22"> <h4>什么是超时赔付？</h4> <p>餐厅页面有“赔”字icon的，则说明该餐厅有参加超时赔付。超时赔付是指：餐厅承诺一个送达时间和一个折扣,从用户下单时间开始计算,如果外卖超过了承诺时间才送到,则该份外卖按照折扣价收取费用。若餐厅有备注：由于恶劣天气、某些美食烹调时间过长、或者其他因素,餐厅可以选择性的延长承诺时间或者不做承诺。</p> </li> 
         <li id="Q23"> <h4>什么是订单数？</h4> <p>订单数是指餐厅最近30天的订单总数，这个数据是动态的，每天都会进行更新。</p> </li> 
         <li id="Q24"> <h4>美食多久内可以点评？点评后是否能修改？</h4> <p>美食一旦点评就不能做修改。如果您有特殊情况需要删除点评，可以联系客服协助操作。</p> </li> 
